import { writable } from 'svelte/store';

export const SUPPORTED_LANGS = ['en', 'ru', 'es', 'ka'];

function detectLang() {
	try {
		const saved = localStorage.getItem('naroom_lang');
		if (saved && SUPPORTED_LANGS.includes(saved)) return saved;
	} catch {}
	try {
		const code = (navigator.language || 'en').slice(0, 2).toLowerCase();
		return SUPPORTED_LANGS.includes(code) ? code : 'en';
	} catch {}
	return 'en';
}

export const lang = writable('en');

export function initLang() {
	lang.set(detectLang());
}

export function setLang(code) {
	if (!SUPPORTED_LANGS.includes(code)) return;
	lang.set(code);
	try { localStorage.setItem('naroom_lang', code); } catch {}
}

/** Russian plural form: returns 'one' | 'few' | 'many' */
export function pluralRu(n) {
	const m10 = Math.abs(n) % 10;
	const m100 = Math.abs(n) % 100;
	if (m10 === 1 && m100 !== 11) return 'one';
	if (m10 >= 2 && m10 <= 4 && (m100 < 10 || m100 >= 20)) return 'few';
	return 'many';
}

